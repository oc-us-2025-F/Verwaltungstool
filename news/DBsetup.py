import sqlite3

def create_news_db(db_path="news.db"):
    conn = sqlite3.connect(db_path)
    cursor = conn.cursor()
    cursor.execute("""
        CREATE TABLE IF NOT EXISTS news (
            id INTEGER PRIMARY KEY AUTOINCREMENT,
            text TEXT NOT NULL,
            created_at TEXT NOT NULL
        )
    """)
    conn.commit()
    conn.close()
    print(f"DB und Tabelle 'news' wurden (falls nötig) unter '{db_path}' erstellt.")

if __name__ == "__main__":
    create_news_db()

